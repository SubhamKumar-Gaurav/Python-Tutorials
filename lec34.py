# Dictionary
# unordered datatype , key : value , { } separated by comma

d={
    'name' : 'Saanp' ,
    'fees' : 100 ,
    'duration' : '2Months'
}

print(d['name'])         # value
print(d['fees'])         # value
print(d['duration'])     # value

print(" ")

# iterating a dictionary 
for a in d :
    print(a)      # key
    print(d[a])   # value





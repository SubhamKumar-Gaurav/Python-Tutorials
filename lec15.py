#  User input and typecasting
# input() , int() , float() , eval() - evaluates anything be it int , float or even binary

a = input("Enter your name : " )
print(a)
b = int(input("Enter integer : "))  # int() function
print(b)
c = float(input("Enter float : "))   # float() function
print(c)
d = eval(input("Enter the 1st value  : "))     # eval() function
e = eval(input("Enter the 2nd value  : "))     # eval() function
print(d+e)



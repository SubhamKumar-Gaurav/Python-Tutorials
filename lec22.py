# Python string function - lower() , upper() , title() , capitalize()

# lower() - used in matching the data from the database i.e. characters
w="Welcome Subham gaurav"
l=w.lower()
print(l)

# upper() - converts all characters to upper case
u=w.upper()
print(u)

# title() - converts the first character of all words into upper case
t=w.title()
print(t)

# capitalize() - converts the first character of first words into upper case
c=w.capitalize()
print(c)






